package my.bean;
//历史信息
public class History {

	private Integer sId;// 历史ID
	private String sName;// 历史名
	private String sAddress;// 历史地址
	private String sTime;// 历史时间
	private String sDesc;// 历史描述
	private String sPath;// 历史图片地址
	private String sNum;// 历史编号同城市cityTn
	/**
	 * @return sId
	 */
	public Integer getsId() {
		return sId;
	}
	/**
	 * @param sId 要设置的 sId
	 */
	public void setsId(Integer sId) {
		this.sId = sId;
	}
	/**
	 * @return sName
	 */
	public String getsName() {
		return sName;
	}
	/**
	 * @param sName 要设置的 sName
	 */
	public void setsName(String sName) {
		this.sName = sName;
	}
	/**
	 * @return sAddress
	 */
	public String getsAddress() {
		return sAddress;
	}
	/**
	 * @param sAddress 要设置的 sAddress
	 */
	public void setsAddress(String sAddress) {
		this.sAddress = sAddress;
	}
	/**
	 * @return sTime
	 */
	public String getsTime() {
		return sTime;
	}
	/**
	 * @param sTime 要设置的 sTime
	 */
	public void setsTime(String sTime) {
		this.sTime = sTime;
	}
	/**
	 * @return sDesc
	 */
	public String getsDesc() {
		return sDesc;
	}
	/**
	 * @param sDesc 要设置的 sDesc
	 */
	public void setsDesc(String sDesc) {
		this.sDesc = sDesc;
	}
	/**
	 * @return sPath
	 */
	public String getsPath() {
		return sPath;
	}
	/**
	 * @param sPath 要设置的 sPath
	 */
	public void setsPath(String sPath) {
		this.sPath = sPath;
	}
	/**
	 * @return sNum
	 */
	public String getsNum() {
		return sNum;
	}
	/**
	 * @param sNum 要设置的 sNum
	 */
	public void setsNum(String sNum) {
		this.sNum = sNum;
	}
	

}
