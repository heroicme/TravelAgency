package my.bean;

public class Products {
	
	private int productId;
	private String productName;
	private String cityNum;
	private int price;
	private int sales;
	private String productDesc;
	private String type;
	private String imgPath;
	private String satisfaction;
	private String supplier;
	/**
	 * @return productId
	 */
	public int getProductId() {
		return productId;
	}
	/**
	 * @param productId 要设置的 productId
	 */
	public void setProductId(int productId) {
		this.productId = productId;
	}
	/**
	 * @return productName
	 */
	public String getProductName() {
		return productName;
	}
	/**
	 * @param productName 要设置的 productName
	 */
	public void setProductName(String productName) {
		this.productName = productName;
	}
	/**
	 * @return cityNum
	 */
	public String getCityNum() {
		return cityNum;
	}
	/**
	 * @param cityNum 要设置的 cityNum
	 */
	public void setCityNum(String cityNum) {
		this.cityNum = cityNum;
	}
	/**
	 * @return price
	 */
	public int getPrice() {
		return price;
	}
	/**
	 * @param price 要设置的 price
	 */
	public void setPrice(int price) {
		this.price = price;
	}
	/**
	 * @return sales
	 */
	public int getSales() {
		return sales;
	}
	/**
	 * @param sales 要设置的 sales
	 */
	public void setSales(int sales) {
		this.sales = sales;
	}
	/**
	 * @return productDesc
	 */
	public String getProductDesc() {
		return productDesc;
	}
	/**
	 * @param productDesc 要设置的 productDesc
	 */
	public void setProductDesc(String productDesc) {
		this.productDesc = productDesc;
	}
	/**
	 * @return type
	 */
	public String getType() {
		return type;
	}
	/**
	 * @param type 要设置的 type
	 */
	public void setType(String type) {
		this.type = type;
	}
	/**
	 * @return imgPath
	 */
	public String getImgPath() {
		return imgPath;
	}
	/**
	 * @param imgPath 要设置的 imgPath
	 */
	public void setImgPath(String imgPath) {
		this.imgPath = imgPath;
	}
	/**
	 * @return satisfaction
	 */
	public String getSatisfaction() {
		return satisfaction;
	}
	/**
	 * @param satisfaction 要设置的 satisfaction
	 */
	public void setSatisfaction(String satisfaction) {
		this.satisfaction = satisfaction;
	}
	/**
	 * @return supplier
	 */
	public String getSupplier() {
		return supplier;
	}
	/**
	 * @param supplier 要设置的 supplier
	 */
	public void setSupplier(String supplier) {
		this.supplier = supplier;
	}
	
	
	
	
	
	
	
	

}
