package my.bean;
//美食信息 为了方便前端选项卡的信息展现 各类别属性名一致
public class Food {

	private Integer sId;// 美食ID
	private String sName;// 美食名
	private String sAddress;// 美食地址
	private String sTime;// 开放时间
	private String sDesc;// 美食描述
	private String sPath;// 美食图片地址
	private String sNum;// 美食编号同城市cityTn
	/**
	 * @return sId
	 */
	public Integer getsId() {
		return sId;
	}
	/**
	 * @param sId 要设置的 sId
	 */
	public void setsId(Integer sId) {
		this.sId = sId;
	}
	/**
	 * @return sName
	 */
	public String getsName() {
		return sName;
	}
	/**
	 * @param sName 要设置的 sName
	 */
	public void setsName(String sName) {
		this.sName = sName;
	}
	/**
	 * @return sAddress
	 */
	public String getsAddress() {
		return sAddress;
	}
	/**
	 * @param sAddress 要设置的 sAddress
	 */
	public void setsAddress(String sAddress) {
		this.sAddress = sAddress;
	}
	/**
	 * @return sTime
	 */
	public String getsTime() {
		return sTime;
	}
	/**
	 * @param sTime 要设置的 sTime
	 */
	public void setsTime(String sTime) {
		this.sTime = sTime;
	}
	/**
	 * @return sDesc
	 */
	public String getsDesc() {
		return sDesc;
	}
	/**
	 * @param sDesc 要设置的 sDesc
	 */
	public void setsDesc(String sDesc) {
		this.sDesc = sDesc;
	}
	/**
	 * @return sPath
	 */
	public String getsPath() {
		return sPath;
	}
	/**
	 * @param sPath 要设置的 sPath
	 */
	public void setsPath(String sPath) {
		this.sPath = sPath;
	}
	/**
	 * @return sNum
	 */
	public String getsNum() {
		return sNum;
	}
	/**
	 * @param sNum 要设置的 sNum
	 */
	public void setsNum(String sNum) {
		this.sNum = sNum;
	}

}
