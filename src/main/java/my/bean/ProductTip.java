package my.bean;

public class ProductTip {
	
	private int tipId;
	private String feature;//产品特色
	private String pDesc;//线路介绍
	private String cost;//费用介绍
	private String notice;//预定须知
	private String comment;//游客评论
	private int pId;//产品ID
	/**
	 * @return tipId
	 */
	public int getTipId() {
		return tipId;
	}
	/**
	 * @param tipId 要设置的 tipId
	 */
	public void setTipId(int tipId) {
		this.tipId = tipId;
	}
	/**
	 * @return feature
	 */
	public String getFeature() {
		return feature;
	}
	/**
	 * @param feature 要设置的 feature
	 */
	public void setFeature(String feature) {
		this.feature = feature;
	}
	/**
	 * @return pDesc
	 */
	public String getpDesc() {
		return pDesc;
	}
	/**
	 * @param pDesc 要设置的 pDesc
	 */
	public void setpDesc(String pDesc) {
		this.pDesc = pDesc;
	}
	/**
	 * @return cost
	 */
	public String getCost() {
		return cost;
	}
	/**
	 * @param cost 要设置的 cost
	 */
	public void setCost(String cost) {
		this.cost = cost;
	}
	/**
	 * @return notice
	 */
	public String getNotice() {
		return notice;
	}
	/**
	 * @param notice 要设置的 notice
	 */
	public void setNotice(String notice) {
		this.notice = notice;
	}
	/**
	 * @return comment
	 */
	public String getComment() {
		return comment;
	}
	/**
	 * @param comment 要设置的 comment
	 */
	public void setComment(String comment) {
		this.comment = comment;
	}
	/**
	 * @return pId
	 */
	public int getpId() {
		return pId;
	}
	/**
	 * @param pId 要设置的 pId
	 */
	public void setpId(int pId) {
		this.pId = pId;
	}
	
	
	
	

}
