package my.bean;

public class CityPicture {

	private Integer pId;//图片id
	private String  pPath;//图片相对于服务器路径
	private String pType;//图片类型
	private String pDesc;//图片描述
	private String pNum;//图片编码 与城市码 cityTn 相对应
	/**
	 * @return pId
	 */
	public Integer getpId() {
		return pId;
	}
	/**
	 * @param pId 要设置的 pId
	 */
	public void setpId(Integer pId) {
		this.pId = pId;
	}
	/**
	 * @return pPath
	 */
	public String getpPath() {
		return pPath;
	}
	/**
	 * @param pPath 要设置的 pPath
	 */
	public void setpPath(String pPath) {
		this.pPath = pPath;
	}
	/**
	 * @return pType
	 */
	public String getpType() {
		return pType;
	}
	/**
	 * @param pType 要设置的 pType
	 */
	public void setpType(String pType) {
		this.pType = pType;
	}
	/**
	 * @return pDesc
	 */
	public String getpDesc() {
		return pDesc;
	}
	/**
	 * @param pDesc 要设置的 pDesc
	 */
	public void setpDesc(String pDesc) {
		this.pDesc = pDesc;
	}
	/**
	 * @return pNum
	 */
	public String getpNum() {
		return pNum;
	}
	/**
	 * @param pNum 要设置的 pNum
	 */
	public void setpNum(String pNum) {
		this.pNum = pNum;
	}
	
	
	
	

}
